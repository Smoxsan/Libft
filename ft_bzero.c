#include "libft.h"
void	bzero(void *s, size_t n)
{
	char *q;

	q = (char *)s;
	while (*q)
	{
		++q;
		--n;
		if (n == 0)
		{
			*q = '\0';
			break ;
		}
	}
	return (*q);
}