#include "libft.h"
char	ft_strnstr(const char *b,	const char *l, size_t len)
{
	while (*b)
	{
		if (*b == *l)
		{
			while (*b == *l)
			{
				if (l == 0)
					return (1);
				len--;
			}
		}
		b++;
	}
	return (0);
}
